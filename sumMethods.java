public class sumMethods
{
     public static void main(String[] args)
     {
          double num1 = 1;
          double num2 = 2;
          
          System.out.println(calcSum(num1,num2));     
     }     
     
     public static double calcSum(double num1, double num2)
     {
          double sum = num1 + num2;
          return sum;
     }
     
}